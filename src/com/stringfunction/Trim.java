package com.stringfunction;

public class Trim {
	
	public static void main(String[] args) {
		
// It will trim the Unwanted Spaces.
		
// It will Cut the Prefix & Suffix spaces Mainly.
		
		String s = "    Welcome to Java    "; // with Prefix and Suffix Unwanted Spaces
		
		System.out.println(s); // with Prefix and Suffix Unwanted Spaces
		
		String tr = s.trim();
		
		System.out.println(tr); // Using trim to Delete the unwanted Prefix & Suffix Spaces.	
		
	}

}
