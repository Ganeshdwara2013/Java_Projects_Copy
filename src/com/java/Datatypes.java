package com.java;

public class Datatypes {

	public static void main(String[] args) {

		//Datatypes
		
		// PRIMITIVE DATA TYPES
		
		//1)Byte ---> -128 to 127
		
		byte a = 127;
		
		System.out.println(a);
		
		//2) Short ---> -32768 to 32767
		
		short b = -32761;
				
	    System.out.println(b);
	    
	    //3) int -----> -2147483648 to 2147483648
	    
	    int c = 1234567890;
	    
	    System.out.println(c);
	    
	    //4) long = -92233720368 to 92233720367L
	    
	    long d = 123456789101112l;
	    
	    System.out.println(d);
	    
	    //5) Float ----->  3.40282347f
	    
	    float x = 122.123456789f;
	    
	    System.out.println(x);
	    
	    //6) double----->   1.797693
	    
	    double y = 12.123456778;
	    		
	    		System.out.println(y);
	    
	    // 7) char - One value either number or character in single quote
	    
	    char z = 'D';
	    		
	    		System.out.println(z);
	    		
	    //8) Boolean - True or False ---0 & 1 is not accepted in Java
	    		
	    boolean i = true;
	    		
	    System.out.println(i);
	    
				
	}

}
