package com.controlstatement;

public class DoWhile {
	
	public static void main(String[] args) {
		
//Do - While Loop is also called as Exit Control Loop.
		
		// Do-While executes the code at least once wihout checking Condition itself.
		
		//Let see using do - while loop to print the below Statement
		
		int i = 1;
		
		do {
			
			System.out.println(i);
			
			i++;
			
		} while(i<=10);
		
		System.out.println("-----******-------");
		
		int j=1000;
		
		do {
			
			System.out.println(j);
			
			j++;
			
		} while(j<=10);
		
		
	}

}
