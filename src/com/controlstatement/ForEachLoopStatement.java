package com.controlstatement;

public class ForEachLoopStatement {
		
// "For Each Loop" is used in Array and Collection topic.
	
// By Using [Array] we are going to see the "For each Loop Statement" Example below.

   public static void main(String[] args) {
	   
	    int a[] = {12,13,14,15};
	    	    
	    for (int x : a) { // For each loop Declaration
	   
// Here, "x" will read all the data's from "a" and it will execute the data.
	 
// It will read the data one by one.
	    	
	    	System.out.println(x);
	    }
	    
	}
   
}