package com.variables;

public class LocalVariable {

	public void localVariable() {
		
//Variable is called as declaration + Initialization
		
// syntax for variable ---> datatype variableName = value;

// Example datatype, int x = 24;

// here int x --->Declaration
			
// x = 24 is ----> Initialization
		
//Rules for Local Variable
		
//must be inside the method
		
//its life cycle will be throughout the method 
		
//we need to initialized the local variable
		

		
		
		int a;
		
		a = 12;
		
		System.out.println(a);
	}
	
	public static void main(String[] args) {
		
		LocalVariable obj = new LocalVariable();
		
		obj.localVariable();
	}
}
